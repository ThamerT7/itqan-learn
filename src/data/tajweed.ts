export const TAJWEED_CATEGORIES: TajweedCategory[] = [
  noonSakinahRules,
  meemSakinahRules,
  maddRules,
  otherRules,
];

export function getTajweedRuleById(ruleId: string): TajweedRule | undefined {
  for (const category of TAJWEED_CATEGORIES) {
    const rule = category.rules.find((r) => r.id === ruleId);
    if (rule) return rule;
  }
  return undefined;
}

export function getCategoryByRuleId(ruleId: string): TajweedCategory | undefined {
  return TAJWEED_CATEGORIES.find((c) => c.rules.some((r) => r.id === ruleId));
}
